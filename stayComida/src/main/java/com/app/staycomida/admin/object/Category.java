package com.app.staycomida.admin.object;

public class Category {
	private Integer scgid;
	private String category_name;
	private String category_kinds;
	private String category_created;
	
	public Integer getScgid() {
		return scgid;
	}
	
	public void setScgid(Integer scgid) {
		this.scgid = scgid;
	}
	
	public String getCategory_name() {
		return category_name;
	}
	
	public void setCategory_name(String category_name) {
		this.category_name = category_name;
	}
	
	public String getCategory_kinds() {
		return category_kinds;
	}
	
	public void setCategory_kinds(String category_kinds) {
		this.category_kinds = category_kinds;
	}
	
	public String getCategory_created() {
		return category_created;
	}
	
	public void setCategory_created(String category_created) {
		this.category_created = category_created;
	}
}
